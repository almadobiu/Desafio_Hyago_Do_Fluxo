package br.com.escola;

public class Matricula {
	private int idMatricula;
	private String nome;
	private double numDaMatricula;
	private int cidade;
	private String rua;
	private String bairro;
	private double cpf;
	private double rg;
	private int idTurmaTurma;
	
	public int getIdMatricula() {
		return idMatricula;
	}
	public void setIdMatricula(int idMatricula) {
		this.idMatricula = idMatricula;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public double getNumDaMatricula() {
		return numDaMatricula;
	}
	public void setNumDaMatricula(double numDaMatricula) {
		this.numDaMatricula = numDaMatricula;
	}
	public int getCidade() {
		return cidade;
	}
	public void setCidade(int cidade) {
		this.cidade = cidade;
	}
	public String getRua() {
		return rua;
	}
	public void setRua(String rua) {
		this.rua = rua;
	}
	public String getBairro() {
		return bairro;
	}
	public void setBairro(String bairro) {
		this.bairro = bairro;
	}
	public double getCpf() {
		return cpf;
	}
	public void setCpf(double cpf) {
		this.cpf = cpf;
	}
	public double getRg() {
		return rg;
	}
	public void setRg(double rg) {
		this.rg = rg;
	}
	public int getIdTurmaTurma() {
		return idTurmaTurma;
	}
	public void setIdTurmaTurma(int idTurmaTurma) {
		this.idTurmaTurma = idTurmaTurma;
	}

	

}
